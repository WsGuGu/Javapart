package javase.ifandelse;

public class If02 {
    public static void main(String[] args) {
        int a=12;
        if(a<5){
            System.out.println("小");
        }
        else if (a<10){
            System.out.println("中");
        }
        else {
            System.out.println("大");
        }
    }
}
